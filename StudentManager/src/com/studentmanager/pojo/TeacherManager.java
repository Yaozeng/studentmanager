package com.studentmanager.pojo;

public class TeacherManager {
	private int teachermanager_id;
	private int course_id;
	private int paper_id;
	private int teacherinformation_id;
	private int fee;
	public int getTeachermanager_id() {
		return this.teachermanager_id;
	}
	public void setTeachermanager_id(int teachermanager_id) {
		this.teachermanager_id = teachermanager_id;
	}
	public int getCourse_id() {
		return this.course_id;
	}
	public void setCourse_id(int course_id) {
		this.course_id = course_id;
	}
	public int getPaper_id() {
		return this.paper_id;
	}
	public void setPaper_id(int paper_id) {
		this.paper_id = paper_id;
	}
	public int getTeacherinformation_id() {
		return this.teacherinformation_id;
	}
	public void setTeacherinformation_id(int teacherinformation_id) {
		this.teacherinformation_id = teacherinformation_id;
	}
	public int getFee() {
		return this.fee;
	}
	public void setFee(int fee) {
		this.fee = fee;
	}
	

}
