package com.studentmanager.pojo;

public class Teacher {
	private int teacherinformation_id;
	private String gender;
	private String name;
	private String tel;
	public int getTeacherinformation_id() {
		return teacherinformation_id;
	}
	public void setTeacherinformation_id(int teacherinformation_id) {
		this.teacherinformation_id = teacherinformation_id;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	

}
